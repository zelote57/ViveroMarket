CREATE PROCEDURE SP_INSERT_PROVEEDOR
	@SWVM01PROV_ID INT, 	
	@SWVM01NOM_EMPRESA VARCHAR(50), 
	@SWVM01NOM_CONTACTO VARCHAR(50), 
	@SWVM01CARGO_CONTACTO VARCHAR(20), 
	@SWVM01RUC VARCHAR(13), 
	@SWVM01DIRECCION VARCHAR(45), 
	@SWVM01TELEFONO1 VARCHAR(10), 
	@SWVM01TELEFONO2 VARCHAR(10), 
	@SWVM01PAIS VARCHAR(15), 
	@SWVM01PROVINCIA VARCHAR(30), 
	@SWVM01CIUDAD VARCHAR(30), 
	@SWVM01EMAIL VARCHAR(80),
	@SWVM01CLAVE VARCHAR(250),
	@SWVM01ESTADO VARCHAR(1), 
	@SWVM01FECHA_REGP DATETIME,
	@SWVM01FECHA_UPDATE DATETIME
AS 
	INSERT INTO SWVMMPROVEEDOR01 
	(	SWVM01PROV_ID, 
		SWVM01NOM_EMPRESA, 
		SWVM01NOM_CONTACTO,
		SWVM01CARGO_CONTACTO,
		SWVM01RUC,
		SWVM01DIRECCION,
		SWVM01TELEFONO1, 
		SWVM01TELEFONO2,
		SWVM01PAIS,
		SWVM01PROVINCIA,
		SWVM01CIUDAD,
		SWVM01EMAIL, 
		SWVM01CLAVE, 
		SWVM01ESTADO, 
		SWVM01FECHA_REGP, 
		SWVM01FECHA_UPDATE)	
	VALUES
	(	@SWVM01PROV_ID, 
		@SWVM01NOM_EMPRESA, 
		@SWVM01NOM_CONTACTO,
		@SWVM01CARGO_CONTACTO,
		@SWVM01RUC,
		@SWVM01DIRECCION,
		@SWVM01TELEFONO1, 
		@SWVM01TELEFONO2,
		@SWVM01PAIS,
		@SWVM01PROVINCIA,
		@SWVM01CIUDAD,
		@SWVM01EMAIL, 
		@SWVM01CLAVE, 
		@SWVM01ESTADO, 
		@SWVM01FECHA_REGP, 
		@SWVM01FECHA_UPDATE)
	
	/*   **********************       */
	
	CREATE PROCEDURE SP_UPDATE_PROVEEDOR
	
	@SWVM01PROV_ID INT, 	
	@SWVM01NOM_EMPRESA VARCHAR(50), 
	@SWVM01NOM_CONTACTO VARCHAR(50), 
	@SWVM01CARGO_CONTACTO VARCHAR(20), 
	@SWVM01RUC VARCHAR(13), 
	@SWVM01DIRECCION VARCHAR(45), 
	@SWVM01TELEFONO1 VARCHAR(10), 
	@SWVM01TELEFONO2 VARCHAR(10), 
	@SWVM01PAIS VARCHAR(15), 
	@SWVM01PROVINCIA VARCHAR(30), 
	@SWVM01CIUDAD VARCHAR(30), 
	@SWVM01EMAIL VARCHAR(80),
	@SWVM01CLAVE VARCHAR(250),
	@SWVM01ESTADO VARCHAR(1), 	
	@SWVM01FECHA_UPDATE DATETIME	
AS
		UPDATE SWVMMPROVEEDOR01
			SET			
			SWVM01NOM_EMPRESA=@SWVM01NOM_EMPRESA,
			SWVM01NOM_CONTACTO=@SWVM01NOM_CONTACTO,
			SWVM01CARGO_CONTACTO=@SWVM01CARGO_CONTACTO,
			SWVM01RUC=@SWVM01RUC,
			SWVM01DIRECCION=@SWVM01DIRECCION, 
			SWVM01TELEFONO1=@SWVM01TELEFONO1,
			SWVM01TELEFONO2=@SWVM01TELEFONO2,
			SWVM01PAIS=@SWVM01PAIS,
			SWVM01PROVINCIA=@SWVM01PROVINCIA,
			SWVM01CIUDAD=@SWVM01CIUDAD,
			SWVM01EMAIL=@SWVM01EMAIL,
			SWVM01CLAVE=@SWVM01CLAVE,
			SWVM01ESTADO=@SWVM01ESTADO, 
			SWVM01FECHA_UPDATE=@SWVM01FECHA_UPDATE 
			WHERE SWVM01PROV_ID=@SWVM01PROV_ID
			
/*   **********************       */

	CREATE PROCEDURE SP_DELETE_PROVEEDOR
	@SWVM01PROV_ID INT,
	@ERROR VARCHAR (5) OUTPUT 	
AS
    DECLARE @ERROR_VAR int 
	DELETE FROM SWVMMPROVEEDOR01
		WHERE (SWVM01PROV_ID=@SWVM01PROV_ID)
	SELECT @ERROR_VAR = @@ERROR
	
	IF (@ERROR_VAR <> 0)
			SET @ERROR = 'ERROR'
	ELSE 
	        SET @ERROR = 'GRABO'